var extensions={
	evol: '.gif',
	comics: '.jpeg', 
	fractals: '.jpeg', 
	chakras: '.jpg', 
	movies: '.jpg',
}
function moreIdx(id){
	if(id==='movies'){
		return 5;
	}else if(id==='comics'){
		return 6;
	}
	return 10;
}
function more(id, min){
	document.getElementById(id).innerHTML=mosaic(id, true)
}

function mosaic(id, more){
	var preview=moreIdx(id),
	 	ext=extensions[id],
		arrList=mediaList[id],
		arrListP = more ? arrList.slice(preview, arrList.length) : arrList.slice(0, preview),
		imageLink = function (m){
			if(m.url){
				return '<a href="'+m.url+'" target="_new"'+(m.css?(' class="'+m.css+'"'):'')+'><img src="pix/'+id+'/'+m.id+ext+'"/></a>';
			}else{
				return '<img src="pix/'+id+'/'+m.id+ext+'"/>';
			}
		}
		mm = arrListP.map(imageLink)
	if(arrList.length>preview & !more){
		mm.push('<span id="'+id+'"><a class="linkMore" href="javascript:more(\''+id+'\','+preview+')">more of my favorite '+id+'...</a></span>');
	}
	if(more && id==='comics'){
		mm.push('<p>Today, you could treat yourself with some excellent French graphic novels like '+
			'<a href="https://smile.amazon.com/Incal-Alejandro-Jodorowsky/dp/1594650934?tag=evolutility-20">L\'Incal</a>, '+
			'<a href="https://smile.amazon.com/Moebius-Library-World-Edena/dp/1506702163?tag=evolutility-20">Edena</a>, '+
			'or <a href="https://smile.amazon.com/Quest-Time-Bird-Serge-Tendre/dp/1782763627?tag=evolutility-20">La QuÃªte de l\'Oiseau du Temps</a> '+
			' available in English translation at Amazon.com.</p>');
		mm.push(mediaList['comics-amazon'].map(imageLink).join(''))
	}
	return mm.join('');
}
