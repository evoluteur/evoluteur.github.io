var linkCaptions={
	comics: 'More graphic novels...',
	'comics-2': 'Even more graphic novels...',
	movies: 'More sci-fi movies...'
}
function pixDir(id){
	if(id.startsWith('comic')){
		return 'pix/comics/'
	}else if(id==='neige'){
		return 'pix/neige/'
	}else{
		return 'pix/'+id+'/'
	}
}

function mosaic(id, more){
	if(id==='comics-2'){
		id='comics';
		more=2;
	}else{
		more= more ? 1 : 0;
	}
	var preview=(id==='movies' || id==='comics')?6:10,
	 	ext=id==='evol'?'.gif':'.jpg',
		arrList=mediaList[id],
		ml = more ? 24 : arrList.length,
		arrListP = more ? arrList.slice(preview, ml) : arrList.slice(0, preview),
		pixPath = pixDir(id),
		imageLink = function (m){
			var pix='<img src="'+pixPath+m.id+ext+'"/>';
			if(m.title){
				pix += '<div class="pixTitle">'+m.title+'</div>';
			}
			var h = '<div class="pixTitleHolder">';
			if(m.url){
				h+= '<a href="'+m.url+'"'+
						(id!='recipes' ? ' target="_blank"':'')+
						(m.css?(' class="'+m.css+'"'):'')+
					'>'+pix+'</a>';
			}else{
				h+= pix;
			}
			h+='</div>';
			return h;
		},
		mm;

		if(more===2){
			arrListP = arrList.slice(ml, arrList.length);
		}else{
			arrListP = more ? arrList.slice(preview, ml) : arrList.slice(0, preview);
		}
		mm = arrListP.map(imageLink);

	if(arrList.length>preview & !more){
		mm.push('<span id="'+id+'_x"><a class="linkMore" href="javascript:more(\''+id+'\','+preview+')">['+linkCaptions[id]+']</a></span>');
	}
	if(more===1 && (id==='comics')){
		mm.push('<span id="'+id+'-2_x"><a class="linkMore" href="javascript:more(\'comics-2\')">[Even more graphic novels...]</a></span>');
		mm.push('<p class="clearer">Did you know that some of the best French graphic novels are now available in English?</p>');
		mm.push(mediaList['comics-amazon'].map(imageLink).join(''))
		mm.push('<p class="clearer">I\'m also getting into American comics.</p>');
		mm.push(mediaList['comics-us'].map(imageLink).join(''))
	}
	mm.push('<div class="clearer"/>');
	return mm.join('');
}

function setMosaic(id){
	document.getElementById(id+'2').innerHTML = mosaic(id);
}
function setPage(){
	['evol','comics','recipes','movies','fractals','chakras'].forEach(setMosaic);
}
